public class arithmatic_op_11 {
    public static void main(String[] args) {
        int a, b;
        a = 34;
        b = 10;
        System.out.println("addition op : a + b : " + (a + b));
        System.out.println("subtraction op : a - b : " + (a - b));
        System.out.println("multiplication op : a * b : " + (a * b));
        System.out.println("division op : a / b : " + (a / b));
        System.out.println("modulus op : a % b : " + (a % b));
    }
}