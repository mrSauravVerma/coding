public class uniary_op_12 {
    public static void main(String[] args) {
        int a = 10;
        int b = 10;
        int c = 10;
        int d = 10;
        System.out.println("pre increment op : ++a : " + (++a)); // 11
        System.out.println("post increment op : b++ : " + (b++)); // 10
        System.out.println("pre decrement op : --c : " + (--c)); // 9
        System.out.println("post decrement op : d-- : " + (d--)); // 10
    }
}
