// all loops in one program 
// for loop
console.log(`this is for loop`);
for (let i = 0; i <= 5; i++) {
    console.log(`index  ${i}`);
}

//forOf loop
console.log(`this is forOf`)
let str="saurav";
for(let char of str){
    console.log(`character ${char}`);
}

// while loop
console.log(`this is while loop`);
let i=0;
while(i<=5){
    console.log(`index ${i}`);
    i++;
}

// doWhile loop
let I=0;
console.log(`this is doWhile loop`);
do{
    console.log(`index ${I}`);
    I++;
}while(I<=5)